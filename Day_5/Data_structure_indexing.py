# Data Structure
# List
#define a list
food = ["pizza", "hamburger", "hotdog", "spaghetti", "pudding"]

# print the list
print(food)

# print the first item in the list
print(food[0])

print(food[-1]) # print from the last item in the list
print(food[-2])

food[1] = "Baryani" #update the 1 index in the list

print(food)

#Tuple

coordinate = (4.21,9.32)
print(coordinate)
print(coordinate[1])
print(coordinate[0])

# Set
fruit_set = {"apple", "banana", "cherry"}
print(fruit_set)

# jasy ham  na list ma index ko update kia tha is ma b kar sakty han

#add item in set
fruit_set.add("orange")
print(fruit_set)

#remove item in set
fruit_set.remove("apple")
print(fruit_set)

#Dictionary
car = {"brand": "Ford", "model": "Mustang", "year": 1964}
print(car)

print(car["brand"])

print(car.get("model"))

car["year"] = 2020 # update the year in the dictionary

print(car)


